package checkpoint1;


public class Associado {

    private String nome;
    private String sobrenome;
    private String email;
    private long CPF;
    private Integer telefone;

    public Associado(String nome, String sobrenome, String email, long CPF, Integer telefone) {
        this.nome = nome;
        this.sobrenome = sobrenome;
        this.email = email;
        this.CPF = CPF;
        this.telefone = telefone;
    }

}

